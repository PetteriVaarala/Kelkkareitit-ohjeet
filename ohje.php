        <h2 id="poi-piirto" class="hlink"><a href="/ohjeet/ohje.php#poi-piirto">Kiinnostaavien kohteiden lisäys (bensa-asemat/laavut/kahviot jne.)</a></h2>
        <ul>
            <li>Mene osoitteeseen <a href="http://www.openstreetmap.org/">http://www.openstreetmap.org</a> ja kirjaudu sisään. Jos sinulla ei ole vielä tunnuksia, luo ne sivun alussa olevien <a href="#tunnus">ohjeiden</a> mukaan.</li>
            <li>Zoomaa karttaa lähelle paikkaa johon haluat lisätä uuden kohteen.</li>
            <li>Valitse sivun yläpalkista "<b>Muokkaa</b>".</li>
            <li>Paina vihreää nuolta:<br /><img src="/ohjeet/img/ohje3-border.png" alt="POI-ohje 1" /></li>
            <li>Klikkaa kartalla kohtaa johon haluat lisätä kohteen. Kartalle ilmestyy <b>punainen pallo</b>, joka on yhdistettynä kursoriin viivalla:<br /><img src="/ohjeet/img/ohje-poi1.png" alt="POI-ohje 2" /></li>
            <li>Klikkaa punaista pistettä, jolloin se muuttuu <b>vihreäksi</b>:<br /><img src="/ohjeet/img/ohje-poi2.png" alt="POI-ohje 2" /></li>
            <li>Nyt kohteelle pitää lisätä tägit, jotka määrittävät sen tyypin. Esimerkiksi bensa-asema määritetään seuraavasti. Paina oikeasta alakulmasta pientä + -nappia ja kirjoita vasemmalle kohtaan "<b>key</b>" -> "<b>amenity</b>" ja kohtaan "<b>(type value here)</b>" -> "<b>fuel</b>". Tägeja voi antaa useampia, paina + -nappia uudelleen ja anna toiseksi tägiksi <b>name=Neste</b>. Lisäksi ne kohteet, jotka ovat hyviä moottorikelkkailun näkökulmasta kannattaa antaa tägi <b>snowmobile=yes</b>.<br /><img src="/ohjeet/img/ohje-poi3.png" alt="POI-ohje 3" /></li>
            <li>Paina lopuksi oikealla olevaa <b>Tallenna</b> painiketta. Ilmestyvässä ikkunassa anna kuvaus lisäyksellesi ja paina OK. Odota kunnes ilmoitetaan "Tietojen lähetys onnistui"</li>
            <li>Valmis!</li>
            <li>Seuraavaksi tiedot prosessoidaan palvelimilla ja se ilmestyy kelkkareitit-sivustolle seuraavan tietokantapäivityksen jälkeen (muutaman päivän kuluttua).</li>
        </ul>

