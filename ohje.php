        <h3 id="reitin-piirto" class="hlink"><a href="/ohjeet/ohje.php#reitin-piirto">Uuden reitin piirtäminen</a></h3>
            <p style="margin:10px 100px;padding:10px;background-color:#ddd">
                HUOM. Tämä ohje solveltuu vain täysin uuden reitin piirtämiseen. Jos haluat muokata olemassa olevaa jälkeä tai lisätä vain osan GPX-jäljestä, ei kannata käyttää automaattitoimintoa vaan piirtää koko reitti käsin. Älä laita ruksia kohdassa 4, ja kun piirto-ohjelma aukeaa paina ikonia <img src="/ohjeet/img/ohje6-small.png" alt="Ohje 6" /> jolloin GPS-jäljet piirtyvät kartalle vaaleasinisinä viivoina. Tämän jälkeen muokkaa/piirrä reitti viivoja apuna käyttäen kartalle.
            </p>
            <ul>
                <li>Lähetä GPS-jälki ylläolevan ohjeen avulla.
                <li>Tallentamisen jälkeen GPS-jälki siirtyy jonoon odottamaan sen käsittelyä. Tässä menee yleensä muutama minuutti, mutta mahdollisesti muutama tuntikin jos palvelin on pahasti ruuhkautunut. Päivitä sivua kunnes JONOSSA -teksti poistuu ja sen tilalle tulee pieni kuva reitistä.
                <br /><img src="/ohjeet/img/ohje2-border.png" alt="Ohje 2" />
                <li>Kun jälki on käsitelty, paina "<b>muokkaa</b>" jäljen kohdalla.
                <li>Laita ruksi kohtaan "<b>Muunna GPX-jälki lukituiksi teiksi..</b>" ja paina vihreää nuolta.
                <br /><img src="/ohjeet/img/ohje3-border.png" alt="Ohje 3" />
                <li>Valitse reitti viemällä kursori punaisen reitin päälle (muuttuu etusormeksi) ja klikkaamalla reittiä. Reitille tulee turkoosi kehys ja reittipisteet tulevat näkyviin.
                <li>Tarkista, että reitti on OK. Jos haluat poistaa reittipisteitä reitiltä, valitse piste ja paina <b>Delete</b>ä näppäimistöltä. Reittipisteiden sijaintia voit siirtää raahamalla hiiren vasemmalla näppäimellä. Näppäimellä <b>Z</b> voit kumota muokkauksia.
                <li>Kun reitti on OK, valitse reitti ja paina punaista "<b>Click to unlock</b>"-nappia.
                <br /><img src="/ohjeet/img/ohje4-border.png" alt="Ohje 4" />
                <li>Enää reitille pitää lisätä tägi josta reitti tunnistetaan kelkkareitiksi. Paina oikeasta alakulmasta pientä <b>+</b> -nappia ja kirjoita vasemmalle kohtaan "<b>key</b>" -> "<b>snowmobile</b>" ja kohtaan "<b>(type value here)</b>" -> "<b>yes</b>".
                <br /><img src="/ohjeet/img/ohje5-border.png" alt="Ohje 5" />
                <li>Reitti on nyt valmis! Paina lopuksi oikealla olevaa <b>Tallenna</b> painiketta. Paina <b>OK</b> ilmestyvässä ikkunassa ja odota kunnes ilmoitetaan "<b>Tietojen lähetys onnistui</b>"
                <li>Valmis!
                <li>Seuraavaksi reitti prosessoidaan palvelimilla ja se ilmestyy sivustolle seuraavan tietokantapäivityksen jälkeen (noin 1-2 päivän kuluttua).
            </ul>
            <p>
                Ohje PDF-muodossa useammilla kuvilla <a href="SnowmobileGPXtoOSM2.pdf">tästä</a>.
            </p>

